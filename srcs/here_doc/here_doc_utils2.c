#include "minishell.h"

int	__eof_type(char *eof)
{

	if (eof[0] == '"' && eof[__strlen(eof) - 1] == '"')
		return (2);
	else if (eof[0] == '\'' && eof[__strlen(eof) - 1] == '\'')
		return (2);
	return (1);
}
